    /** Enables the buttons only when a manager is logged in */
    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown

        if(ManagementUseCase.getInstance().checkManagerActivity() == true)
        {
            addTitleButton.setEnabled(true);
            addCopyButton.setEnabled(true);
            titleReportButton.setEnabled(true);
        }
        else
        {
            addTitleButton.setEnabled(false);
            addCopyButton.setEnabled(false);
            titleReportButton.setEnabled(false);            
        }
    }//GEN-LAST:event_formComponentShown
    
