    /** Creates a new rentable item associated with a selected title */
    private void addCopyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCopyButtonActionPerformed
        if(titleList.getSelectedValue() != null)
        {
            String titleName = titleList.getSelectedValue().toString();
            String itemId = "" + (StoreDatabase.getInstance().getNextItemId());
            ManagementUseCase.getInstance().startAddRentableItem(titleName);
            GUI.getInstance().showMessage("Copy of: " + titleName + " added with ID# " + itemId);
        }
        else
            GUI.getInstance().showMessage("Please select a title from the list");
    }//GEN-LAST:event_addCopyButtonActionPerformed

